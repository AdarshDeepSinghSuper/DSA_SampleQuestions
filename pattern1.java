public class pattern1 {
    public static void main(String[]args){
        //row=4,column=5
        int r=4,c=5;
        //printing rows
        for(int i=0;i<r;i++){
            for(int j=0;j<c;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
